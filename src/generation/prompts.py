def _format_hit(idx: int, hit: RankedHit) -> str:
    h = hit.hit
    header_parts = [p for p in (h.law_number, h.article, h.paragraph) if p]
    header = " ".join(header_parts) if header_parts else "(χωρίς μεταδεδομένα)"
    pages = (
        f"{h.page_start}-{h.page_end}"
        if h.page_start is not None and h.page_end is not None
        else "—"
    )
    return (
        f"=== chunk_{idx + 1} ===\n"
        f"Πηγή: {header}\n"
        f"Σελίδες: {pages}\n"
        f"Αρχείο: {h.source_file or '—'}\n"
        f"\n"
        f"{h.text or ''}\n"
        f"==="
    )


def build_user_message(query: str, hits: list[RankedHit]) -> str:
    """Render the user-turn message from the query and retrieved hits."""
    chunks_block = "\n\n".join(_format_hit(i, h) for i, h in enumerate(hits))
    return (
        f"Ερώτηση: {query}\n"
        f"\n"
        f"Διαθέσιμα αποσπάσματα:\n"
        f"\n"
        f"{chunks_block}\n"
        f"\n"
        f"Παρακαλώ απάντησε στην ερώτηση βάσει αποκλειστικά των παραπάνω αποσπασμάτων."
    )
